# Imports native math module
import math

# Prints a basic given number
print(-2.0987)

# Simple division
print(3 / 4.5)

# Simple multiplication
print(3 * 4.5)

# Order of operations
print(3 * 4 + 5)
print(3 * (4 + 5))

# Divides two given values & returns the remainder
print(19 % 3)

# Numbers can be stored inside variables
my_num = 5

# Converts a number into a string
print(str(my_num) + " is my favourite number")

# Returns the absolute value of a given number. Turns any negative number into a positive by removing the negative symbol.
my_num = -17
print(abs(my_num))

# Returns the power of a value by another value. As in below, the return is 3 to the power of 2
print(pow(4, 6))

# Returns the larger of the two numbers that have been passed
print(max(4, 23))

# Returns the smallest of the two numbers that have been passed
print(min(7, 125))

# Rounds a value either up or down. .5 values gets rounded up instead of down
print(round(23.5))

# Chops off any decimal number
print(math.floor(3.7))

# Rounds any number up regardless if the decimal number being less than 5
print(math.ceil(3.2))

# Returns the square root of a given number
print(math.sqrt(32))

# Returns the result as an integer
print (10 // 3)

# Returns the result of an exponent. In this case, 10 to the power of 3
print (10 **3)

# Augmented  assignment operator.
x = 10
x = x + 3 # Or
x += 3  # Same as above

print(math.ceil(2.2))